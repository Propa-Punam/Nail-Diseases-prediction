import torch
import torch.nn as nn
import timm

class VisionTransformerClassifier(nn.Module):
    def __init__(self, input_dim, num_classes=1):
        super(VisionTransformerClassifier, self).__init__()
        self.vit = timm.create_model('vit_base_patch16_224', pretrained=True, num_classes=num_classes)
        self.fc = nn.Linear(input_dim, num_classes)
        self.dropout = nn.Dropout(p=0.5)  # Fine-tuned dropout rate

    def forward(self, x):
        x = self.dropout(x)  # Apply dropout before the final layer
        x = self.fc(x)
        return x