import torch
from torch.utils.data import DataLoader
import numpy as np
import matplotlib.pyplot as plt

# Function to prepare the data loader
def prepare_dataloaders(dataset, batch_size, num_workers=4):
    train_size = int(0.8 * len(dataset))
    val_size = len(dataset) - train_size
    train_dataset, val_dataset = torch.utils.data.random_split(dataset, [train_size, val_size])

    train_loader = DataLoader(train_dataset, batch_size=batch_size, shuffle=True, num_workers=num_workers)
    val_loader = DataLoader(val_dataset, batch_size=batch_size, shuffle=False, num_workers=num_workers)

    return train_loader, val_loader

# Function to plot metrics
def plot_metrics(train_data, val_data, metric_name, file_name):
    plt.figure(figsize=(10, 5))
    plt.plot(train_data, label=f'Train {metric_name}')
    plt.plot(val_data, label=f'Validation {metric_name}')
    plt.title(f'{metric_name} vs Epochs')
    plt.xlabel('Epochs')
    plt.ylabel(metric_name)
    plt.legend()
    plt.savefig(file_name)
    plt.show()